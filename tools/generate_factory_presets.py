def value_string(value):
    if isinstance(value, float):
        return f"{value:.6g}"
    return str(value)


def clamp(value, minimum, maximum):
    return max(minimum, min(maximum, value))


def derive_performance_macros(params, explicit_params):
    derived = dict(params)

    if "macro_5" not in explicit_params:
        derived["macro_5"] = clamp((params.get("sub_level", 0.0) * 0.45) + (0.12 if params.get("mono_mode", 0) else 0.0), 0.0, 0.8)

    if "macro_6" not in explicit_params:
        derived["macro_6"] = clamp(params.get("fx_pump_depth", 0.0) + 0.08 if params.get("fx_pump_enabled", 0) else 0.0, 0.0, 0.7)

    if "macro_7" not in explicit_params:
        derived["macro_7"] = clamp((abs(params.get("osc2_tune", 0.0)) / 12.0)
                                   + (params.get("macro_3", 0.0) * 0.4)
                                   + (params.get("unison_detune", 0.0) * 1.8), 0.0, 0.7)

    if "macro_8" not in explicit_params:
        derived["macro_8"] = clamp(params.get("macro_4", 0.0) * 0.82, 0.0, 0.75)

    return derived


def write_preset(preset):
    params = DEFAULTS | preset["params"]
    metadata = FACTORY_METADATA.get(preset["name"], {})
    if "filter_character" not in preset["params"]:
        params["filter_character"] = FILTER_CHARACTER_BY_PRESET.get(preset["name"], 0)
    if "filter_slope" not in preset["params"]:
        params["filter_slope"] = FILTER_SLOPE_BY_PRESET.get(preset["name"], 0)
    if "osc_warp" not in preset["params"]:
        params["osc_warp"] = OSC_WARP_BY_PRESET.get(preset["name"], 0.0)
    lock_settings = SEQUENCER_LOCK_SETTINGS.get(preset["name"], {})
    if "sequencer_lock_destination" not in preset["params"]:
        params["sequencer_lock_destination"] = lock_settings.get("destination", 0)
    if "sequencer_lock_depth" not in preset["params"]:
        params["sequencer_lock_depth"] = lock_settings.get("depth", 0.35)
    if params.get("fx_pump_enabled", 0) and "fx_pump_curve" not in preset["params"]:
        category = preset.get("category", "UKG")
        params["fx_pump_curve"] = {
            "UKG": 2,
            "Minimal": 4,
            "Tech House": 1,
            "Techno": 1,
            "House": 0,
        }.get(category, 0)
    if params.get("fx_delay_enabled", 0) and "fx_delay_sync" not in preset["params"]:
        params["fx_delay_sync"] = 1
        params["fx_delay_rate"] = preset["params"].get("fx_delay_rate", 1)
    params = derive_performance_macros(params, preset["params"])
    steps = apply_step_locks(preset["name"], preset["steps"])
    root = ElementTree.Element(
        "PARAMETERS",
        {
            "nate_vst_state_version": "1",
            "sample_file": "",
            "preset_name": preset["name"],
            "preset_category": preset.get("category", "UKG"),
            "preset_author": "Nate",
            "preset_source": "Factory",
            "preset_pack": preset.get("pack", metadata.get("pack", "Factory Pack")),
            "preset_key": preset.get("key", metadata.get("key", "Any Key")),
            "preset_bpm": str(preset.get("bpm", metadata.get("bpm", 0))),
            "preset_tags": preset.get("tags", preset_tags(preset, params)),
            "preset_notes": preset.get("notes", ""),
            **steps,
        },
    )

    for param_id, value in params.items():
        ElementTree.SubElement(root, "PARAM", {"id": param_id, "value": value_string(value)})

    tree = ElementTree.ElementTree(root)
    ElementTree.indent(tree, space="  ")
    destination = OUTPUT_DIR / f"{preset['name']}.natevstpreset"
    tree.write(destination, encoding="utf-8", xml_declaration=True)


def main():
    OUTPUT_DIR.mkdir(parents=True, exist_ok=True)
    for preset in PRESETS:
        write_preset(preset)


if __name__ == "__main__":
    main()
